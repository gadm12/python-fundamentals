user_input = input("enter a 6 letter word: ")

print(f"Original word: {user_input}")

print(f"First Character: {user_input[0]}")

print(f"Last Character: {user_input[-1]}")

print(f"First three Characters: {user_input[0:3]}")

print(f"last three Characters: {user_input[3:]}")

print(f"All letters between first and last letters: {user_input[1:-1]}")
